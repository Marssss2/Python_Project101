#Membersship operators "in" and "not in" are used to check if a value is present in a sequence (like a string, list, tuple, etc.) or not.


print('abc' in ['abc', 'def', 'ghi']) # it will return true because 'abc' is present in the list.
print( 'o' not in 'Hello') # it will return false because 'o' is present in the string "Hello".