letters = ['a', 'b', 'c', 'd', 'e']
print(list(map(str.upper, letters))) #it will convert each element in the list to uppercase using the map function and print the resulting list

Number = [1, 2, 3, 4, 5]
print(list(map(int, Number))) #it will convert each element in the list to an integer using the map function and return a new list of integers
