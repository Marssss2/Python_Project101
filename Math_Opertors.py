print(2 + 3) #it will give you the sum of 2 and 3
print(10 - 4) #it will give you the difference of 10 and 4
print(5 * 6) #it will give you the product of 5 and 6
print(20 / 7)   #division it will give you the result with decimal
print(20 // 3)   #floor division it will round down the result to the nearest whole number
print(2 ** 3)    #exponentiation it will raise the first number to the power of the second number
print(10 % 3)    #modulus it will give you the remainder of the division of 10 by 3

x = 2
x += 3 #it is a shorthand operator that adds 3 to the current value of x and assigns the result back to x
print(x) #it will print the current value of x which is 8

x -= 2 #it is a shorthand operator that subtracts 2 from the current value of x and assigns the result back to x
print(x) #it will print the current value of x which is 6   

x *= 4 #it is a shorthand operator that multiplies the current value of x by 4 and assigns the result back to x
print(x) #it will print the current value of x which is 24  