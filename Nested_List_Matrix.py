matrix = [[1, 2, 3], 
         [4, 5, 6],
         [7, 8, 9]]


print(matrix) # it will print the entire matrix, which is [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

mixed_matrix = [[1, 'two', 3.0],
                ['four', 5, 6.0],
                [7.0, 'eight', 9]]
print(mixed_matrix) # it will print the entire mixed matrix, which is [[1, 'two', 3.0], ['four', 5, 6.0], [7.0, 'eight', 9]]