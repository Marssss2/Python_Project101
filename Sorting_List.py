random_Numbers = [5, 2, 9, 1, 5, 6]

random_Numbers.sort() #it sorts the list in ascending order. The sort() method modifies the original list and does not return a new list.
print(random_Numbers)

random_Numbers.sort(reverse=True)
print(random_Numbers)

