scores = [80, 90, 70, 85, 95]
total = 0
for score in scores:
    total += score          
    print(f"Current Total: {total}")
print(f"Final Total: {total}")
# The for loop iterates over each score in the scores list, adding it to the total variable. The current total is printed after each addition, and the final total is printed at the end of the loop.   

